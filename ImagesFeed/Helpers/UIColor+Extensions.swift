import UIKit

extension UIColor {
    static var YPBackground: UIColor {
        UIColor(named: "YPBackground") ?? UIColor.black
    }
    static var YPBlack: UIColor {
        UIColor(named: "YPBlack") ?? UIColor.black
    }
    static var YPBlue: UIColor {
        UIColor(named: "YPBlue") ?? UIColor.blue
    }
    static var YPGray: UIColor {
        UIColor(named: "YPGray") ?? UIColor.gray
    }
    static var YPRed: UIColor {
        UIColor(named: "YPRed") ?? UIColor.red
    }
    static var YPWhiteAlpha50: UIColor {
        UIColor(named: "YPWhiteAlpha50") ?? UIColor.white
    }
}
